import numpy as np

a=np.array([[1,2],
            [3,4]])

b=np.array([[4,3],
            [2,1]])

#Adding 1 to every element in array
print("Added 1 to each element in an array \'a\' \n",a+1)

#Subtracting 2 to every element in array
print("Subtracted 2 from each element in an array \'b\' \n",b-2)

#Summing up all array elements 
print("Sum of array elements of a \n", a.sum())

#adding two arays
print("Addition of two arrays: \n", a+b)